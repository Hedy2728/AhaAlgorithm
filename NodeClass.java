package AhaAlgorithm;

public class NodeClass {
    public int x;
    public int y;
    public int f;
    public int s;

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }

    public void setF(int f) {
        this.f = f;
    }

    public void setS(int s) {
        this.s = s;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getF() {
        return f;
    }

    public int getS() {
        return s;
    }
}

